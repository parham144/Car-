// Safe utility to cleanly post-process generated telemetry, adding +30% to all parts and clearing "اوس عماد" from carModelName
function postProcessOutput(output: any): any {
  if (!output) return output;

  // 1. Clean carModelName
  if (typeof output.carModelName === "string") {
    output.carModelName = output.carModelName.replace(/اوس\s+عماد\s*[-–:\s]*\s*/gi, "").trim();
  }

  // 2. Increase all prices by 30% (+30%)
  if (Array.isArray(output.categories)) {
    output.categories.forEach((cat: any) => {
      if (Array.isArray(cat.parts)) {
        cat.parts.forEach((part: any) => {
          if (part.priceRange && typeof part.priceRange === "object") {
            if (typeof part.priceRange.min === "number") {
              part.priceRange.min = Math.round(part.priceRange.min * 1.30);
            }
            if (typeof part.priceRange.max === "number") {
              part.priceRange.max = Math.round(part.priceRange.max * 1.30);
            }
          }
        });
      }
    });
  }

  // 3. Recount total parts dynamically
  let totalParts = 0;
  if (Array.isArray(output.categories)) {
    output.categories.forEach((cat: any) => {
      if (Array.isArray(cat.parts)) {
        totalParts += cat.parts.length;
      }
    });
  }
  if (totalParts > 0) {
    output.partsCount = totalParts;
  }

  return output;
}

